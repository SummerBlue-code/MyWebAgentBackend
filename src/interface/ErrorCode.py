from enum import Enum

class ErrorCode(Enum):
    """错误码枚举"""
    # 认证相关错误 (1000-1099)
    AUTH_MISSING_USER_ID = 1001
    AUTH_MISSING_PASSWORD = 1002  # 改为密码
    AUTH_INVALID_FORMAT = 1003
    AUTH_USER_NOT_FOUND = 1004
    AUTH_TIMEOUT = 1005
    AUTH_USER_ALREADY_EXISTS = 1006  # 用户已存在
    AUTH_INVALID_USERNAME = 1007  # 无效的用户名
    AUTH_INVALID_PASSWORD = 1008  # 无效的密码
    AUTH_WRONG_PASSWORD = 1009  # 密码错误
    AUTH_MISSING_TYPE = 1010  # 缺少认证类型
    AUTH_MISSING_USERNAME = 1011
    
    # 消息处理错误 (1100-1199)
    MSG_INVALID_TYPE = 1101
    MSG_JSON_PARSE_ERROR = 1102
    MSG_GPT_RESPONSE_ERROR = 1103
    
    # 工具执行错误 (1200-1299)
    TOOL_MISSING_ID = 1201
    TOOL_MISSING_NAME = 1202
    TOOL_MISSING_PARAMS = 1203
    TOOL_MISSING_ADDRESS = 1204
    TOOL_PARAMS_FORMAT_ERROR = 1205
    TOOL_TIMEOUT = 1206
    TOOL_HTTP_ERROR = 1207
    TOOL_EXECUTION_ERROR = 1208
    
    # 心跳相关错误 (1300-1399)
    HEARTBEAT_TIMEOUT = 1301
    HEARTBEAT_MAX_RETRIES = 1302
    
    # 服务器内部错误 (1400-1499)
    SERVER_INTERNAL_ERROR = 1401
    SERVER_CONNECTION_ERROR = 1402 
    
    INVALID_PARAMETER = 1501
